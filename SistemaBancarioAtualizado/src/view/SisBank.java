    private void setEstado(int estado) {
        if (estado == CONTA_ABERTA) {
            jBAberturaConta.setEnabled(false);
            jBDepositar.setEnabled(true);
            jBSacar.setEnabled(true);
            jBLimparCampos.setEnabled(true);
        } else if (estado == CONTA_FECHADA) {
            jBAberturaConta.setEnabled(true);
            jBDepositar.setEnabled(false);
            jBSacar.setEnabled(false);
            jBLimparCampos.setEnabled(true);
        }
    }

