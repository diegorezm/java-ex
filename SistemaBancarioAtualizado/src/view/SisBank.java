    private void jBAberturaContaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAberturaContaActionPerformed
        LocalDate dataAbertura = LocalDate.now();

        if (Util.removeMascara(jFTFNumeroConta.getText().trim()).isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Preencha o Nº da CONTA!!!!",
                    "Sistema Bancário",
                    JOptionPane.ERROR_MESSAGE);
            jFTFNumeroConta.requestFocusInWindow();
            return;
        }

        if (jTFNomeTitular.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Preencha o NOME do titular da conta!!!!",
                    "Sistema Bancário",
                    JOptionPane.ERROR_MESSAGE);
            jTFNomeTitular.requestFocus();
            return;
        }
        if (jTFSobrenomeTitular.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Preencha o SOBRENOME do titular da conta!!!!",
                    "Sistema Bancário",
                    JOptionPane.ERROR_MESSAGE);
            jTFSobrenomeTitular.requestFocus();
            return;
        }

        if (Util.removeMascara(jFTFCpf.getText()).trim().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Preencha o CPF do titular da conta!!!!",
                    "Sistema Bancário",
                    JOptionPane.ERROR_MESSAGE);
            jFTFCpf.requestFocusInWindow();
            return;
        }

        if (bGTipoConta.getSelection() != null) {
            if (jRBContaSimples.isSelected()) {
                conta = new ContaCorrente(jFTFNumeroConta.getText(), new Pessoa(jTFNomeTitular.getText(), jTFSobrenomeTitular.getText(), jFTFCpf.getText()), dataAbertura);
            } else if (jRBContaEspecial.isSelected()) {
                conta = new ContaEspecial(jFTFNumeroConta.getText(), new Pessoa(jTFNomeTitular.getText(), jTFSobrenomeTitular.getText(), jFTFCpf.getText()), dataAbertura);
            }

            jTAInformacoes.setText(conta.toString());
            JOptionPane.showMessageDialog(this, "Abertura de Conta realzada com sucesso!!!!", "Sistema Bancário", 1);
            setEstado(1);
        } else {
            JOptionPane.showMessageDialog(this, "Please, selecione o tipo da conta!", "Aviso", 0);
        }
    }//GEN-LAST:event_jBAberturaContaActionPerformed

    private void jBSacarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBSacarActionPerformed
        double valorSaque = Double.parseDouble(JOptionPane.showInputDialog(null, "Informe o valor a ser sacado", "Sistema Bancário", 3));

        if (conta.sacar(valorSaque) == false){
            JOptionPane.showMessageDialog(this, "SALDO INSUFICIENTE", "Sistema Bancário", 0);
        } else {
            JOptionPane.showMessageDialog(this, "SAQUE REALIZADO COM SUCESSO", "Sistema Bancário", 1);
            jTAInformacoes.setText(conta.toString());

        }
    }//GEN-LAST:event_jBSacarActionPerformed

    private void jBDepositarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBDepositarActionPerformed
      
        double valorDeposito = Double.parseDouble(JOptionPane.showInputDialog(this, "Informe o valor que deseja depositar", 
                                                                                                              "Sistema Bancário - Operação DEPÓSITO", 1));

        if (valorDeposito < 0){
             JOptionPane.showMessageDialog(this, "Valor inválido!", "Sistema Bancário", 0);
             return;
        }
        
        conta.depositar(valorDeposito);

        JOptionPane.showMessageDialog(this, "DEPÓSITO REALIZADO COM SUCESSO", "Sistema Bancário", 1);

        jTAInformacoes.setText(conta.toString());

   

    }//GEN-LAST:event_jBDepositarActionPerformed

